"""
占位符
%s 将内容转换成字符串
# %d 将内容转换成整数
# %f 将内容转换成浮点型
"""

name = "Iven"
age = 21
gpa = 3.59
print("我的名字是:%s ,我的年龄为：%d岁, 我的GPA为:%f" % (name, age, gpa))